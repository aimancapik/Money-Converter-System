public class MyLogin { //to login in the system
    private int userId;
    private String username;
    private String password;
    
    public int getUserId() { //to get userId
        return userId;
    }

    public void setUserId(int userId) { //to set userId
        this.userId = userId;
    }

    public String getUsername() { //to get username
        return username;
    }

    public void setUsername(String username) { //to set username
        this.username= username;
    }

    public String getPassword() { //to get password
        return password;
    }

    public void setPassword(String password) {//to set password
        this.password = password;
    }

    
}
